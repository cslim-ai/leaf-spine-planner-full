  function getDictionary(locale) {
    return dictionaries[SUPPORTED_LOCALES.includes(locale) ? locale : DEFAULT_LOCALE];
  }

  function getValue(path, locale = DEFAULT_LOCALE) {
    return path.split(".").reduce((value, key) => value?.[key], getDictionary(locale));
  }

  function format(template, params = {}) {
    return String(template ?? "").replace(/\{(\w+)\}/g, (match, key) => (
      Object.prototype.hasOwnProperty.call(params, key) ? params[key] : match
    ));
  }

  function t(path, params = {}, locale = DEFAULT_LOCALE) {
    const value = getValue(path, locale) ?? getValue(path, DEFAULT_LOCALE) ?? path;
    return format(value, params);
  }

  function detectLocale(language = globalThis.navigator?.language) {
    const normalized = String(language || "").toLowerCase();
    if (normalized.startsWith("ko")) return "ko";
    if (normalized.startsWith("en")) return "en";
    return DEFAULT_LOCALE;
  }

  return {
    DEFAULT_LOCALE,
    SUPPORTED_LOCALES,
    dictionaries,
    detectLocale,
    format,
    getDictionary,
    t,
  };
